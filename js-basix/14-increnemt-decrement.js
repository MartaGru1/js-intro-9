// these are ++ or -- and used to increse or decrese number by 1

let n1 = 10, n2 = 10, n3 = 10, n4 = 10;

console.log(n1++); // 10 -> variable++ is known as post-increment
console.log(++n2); // 11 -> ++variable is known as pre-increment

console.log(n1); //11
console.log(n2); //11


console.log(n3--); // 10
console.log(--n4); // 9

console.log(n3); //9
console.log(n4); //9


let number = 5;
console.log(number++ * 10);  //50

let number = 5;
number++ //5  5+1 becomes 6
--number // became 6, but since minus is at the begining, that means already 5;
number *= 2; //10

console.log(++number ** 2);   // 121

